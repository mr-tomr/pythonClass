# Examples and explanations of Python Math Operators

# Define two numbers
num1 = 10
num2 = 3

# Addition: Adds two numbers
addition = num1 + num2
print("Addition:", addition)

# Subtraction: Subtracts second number from the first
subtraction = num1 - num2
print("Subtraction:", subtraction)

# Multiplication: Multiplies two numbers
multiplication = num1 * num2
print("Multiplication:", multiplication)

# Exponentiation: Raises the first number to the power of the second
exponentiation = num1 ** num2
print("Exponentiation:", exponentiation)

# Float Division: Divides the first number by the second, result as a float
float_division = num1 / num2
print("Float Division:", float_division)

# Floor Division: Divides the first number by the second, result as an integer (rounded down)
floor_division = num1 // num2
print("Floor Division:", floor_division)

# Modulo: Returns the remainder of dividing the first number by the second
modulo = num1 % num2
print("Modulo:", modulo)
